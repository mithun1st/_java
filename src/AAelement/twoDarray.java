
package AAelement;


public class twoDarray {

    public static void main(String[] args) {
        int[][] n= new int[3][5];
        
        for (int i = 0; i < n.length; i++) {
            for (int j = 0; j < n[0].length; j++) {
                System.out.print("* ");
            }
            System.out.println("");
        }
    }

}
